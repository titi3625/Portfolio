<h2>Production d'une solution logicielle et d'infrastructure</h2>
<p>J'ai réaliser différents projets consistant à developper des logiciels</p>

<h4>Première activité en classe</h4>
<p>
	Dans cette activité j'ai dut développer avec mon groupe une interface de configuration pour le site marchand de GSB. 
	Ce site marchand vend des médicaments et nous devions prendre en charge la gestion des commandes, des produits et des utilisateurs du site. <br>
	Nous avons d'abord conçu la base de données puis nous avons développé l'interface graphique du site (en HTML et CSS).
	Ensuite nous avons mis en place l'interaction avec la base de donnée à l'aide du langage SQL et mis en forme les informations sous la forme de tableaux dynamiques
	en utilisant le langage PHP. <br>
	Le site est accessible à l'adresse : <a href="http://gsbadmin.franceserv.com/">http://gsbadmin.franceserv.com</a>
</p>

<h4>Deuxième activité en classe</h4>
<p>
	Cette activité avait le même cahier des charges que la précédente sauf que nous devions reproduire le logiciel sous la forme d'une application autonome et en utilisant le langage C#.
	La difficulté était qu'aucun membre de notre groupe n'avait d'experience dans ce langage. <br>
	Nous avons alors du faire des recherches et lire beaucoup de documentation pour comprendre ce langage (qui ressemble beaucoup au Java que nous utilisons souvent) et pour mettre en place les même fonctions que pour le projet précédent. Le plus compliqué a été de comprendre les interactions avec la base de données qui s'effectuaient à l'aide d'un composant spécifique extérieur au langage.
</p>

<h4>Activité pendant le stage de deuxième année</h4>
<p>
	Pendant ce stage de 8 semaines j'ai eu pour mission de concevoir et développer un logiciel de gestion de courrier. Ce logiciel devait permettre d'améliorer un système déja existant qui consistait à remplir une feuille Excel manuellement chaque matin et l'imprimer. Le logiciel devait donc permettre de stocker les données dans une base de données pour ensuite pouvoir faire des recherches selon différents critères. <br>
	Il devait également prendre en charge les types de courriers, les accusés de réceptions et devait posséder une interface d'administration.
	Le logiciel devait être réalisé en PHP et la base de données en MySql.
</p>

